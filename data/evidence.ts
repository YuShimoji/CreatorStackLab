export function listClaimsForEntity(entityId: string): Claim[] {
  return evidenceClaims.filter((claim) => claim.entityId === entityId);
}

export function listEvidenceForClaim(claimId: string): EvidenceReference[] {
  const claim = evidenceClaims.find((item) => item.claimId === claimId);
  if (!claim) return [];
  return evidenceReferences.filter((evidence) => claim.evidenceRefs.includes(evidence.evidenceId));
}

export function listEvidenceForEntity(entityId: string): EvidenceReference[] {
  return evidenceReferences.filter((evidence) => evidence.entityId === entityId);
}

export function getEvidenceCoverage(entityId: string): EvidenceCoverage {
  return coverageByEntity[entityId] ?? {
    operatorFieldTest: false,
    officialDocumentation: false,
    publisherTestInformation: false,
    thirdPartyFieldReports: false,
    conditionsIdentified: false,
  };
}
